/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
    /* MCU Configuration--------------------------------------------------------*/
    HAL_Init();
    SystemClock_Config();

    MX_GPIO_Init();
    MX_USART2_UART_Init();
    MX_SPI2_Init();
    MX_FATFS_Init();

    /* USER CODE BEGIN 2 */
    myprintf("\r\n~ SD card test demo ~\r\n\r\n");
    HAL_Delay(500);

    // --- Mount SD card ---
    fres = f_mount(&FatFs, "", 1);
    if(fres != FR_OK) {
        myprintf("f_mount error: %i\r\n", fres);
        while(1);
    }

    // --- SD card statistics ---
    DWORD free_clusters, free_sectors, total_sectors;
    FATFS* getFreeFs;

    fres = f_getfree("", &free_clusters, &getFreeFs);
    if(fres != FR_OK) {
        myprintf("f_getfree error: %i\r\n", fres);
        while(1);
    }

    total_sectors = (getFreeFs->n_fatent - 2) * getFreeFs->csize;
    free_sectors  = free_clusters * getFreeFs->csize;

    myprintf("SD card stats:\r\n%10lu KiB total.\r\n%10lu KiB free.\r\n",
             total_sectors/2, free_sectors/2);

    // --- Write file ---
    fres = f_open(&fil, "write.txt", FA_WRITE | FA_CREATE_ALWAYS);
    if(fres == FR_OK) {
        myprintf("Opened 'write.txt' for writing\r\n");
        const char *text = "Hello, STM32 SD card test!\r\n";
        UINT bytesWrote;
        fres = f_write(&fil, text, strlen(text), &bytesWrote);
        if(fres == FR_OK) myprintf("Wrote %u bytes to 'write.txt'\r\n", bytesWrote);
        else myprintf("f_write error: %i\r\n", fres);
        f_close(&fil);
    } else myprintf("f_open error: %i\r\n", fres);

    // --- Read file back ---
    fres = f_open(&fil, "write.txt", FA_READ);
    if(fres == FR_OK) {
        char buf[64];
        if(f_gets(buf, sizeof(buf), &fil) != NULL) {
            myprintf("Read from SD: %s\r\n", buf);
        }
        f_close(&fil);
    } else myprintf("f_open read error: %i\r\n", fres);

    // --- De-mount ---
    f_mount(NULL, "", 0);

    /* USER CODE END 2 */

    /* Infinite loop */
    while (1)
    {
        HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
        HAL_Delay(1000);
    }
}
