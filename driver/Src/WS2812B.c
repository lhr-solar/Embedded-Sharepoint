status_t WS2812B_Send(uint8_t colors[], uint8_t num_leds) {
    uint8_t sig_gen[num_leds*24];
    uint16_t idx=0;

    for (int i=0; i<num_leds*24; i++) {
        if (colors[i]) {
            sig_gen[idx++]=SEND_HIGH;      // ASSERT HIGH
        } else sig_gen[idx++]=SEND_LOW;    // ASSERT LOW
    }

    HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t*) sig_gen, 24*num_leds);
    send_semphr=0; // take
    while (!send_semphr) { } 

    return STATUS_SUCCESS;
}

status_t WS2812B_Reset(GPIO_TypeDef* port, uint16_t pin) {
    uint8_t low_reset[RST_NUM_PERIODS]={0};
    HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t*) low_reset, RST_NUM_PERIODS);
    send_semphr=0; // take
    while (!send_semphr) { }

    return STATUS_SUCCESS;
}


status_t WS2812B_Init(GPIO_TypeDef* port, uint16_t pin) {
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init(port, pin);
    MX_DMA_Init();
    MX_TIM1_Init();

    return STATUS_SUCCESS;
}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef* htim)
{
	HAL_TIM_PWM_Stop_DMA(htim, TIM_CHANNEL_1);
    send_semphr=1; // release
}

