status_t WS2812B_Send(uint8_t colors[], uint8_t num_leds) {
    uint8_t sig_gen[num_leds*24];
    uint16_t idx=0;

    for (int i=0; i<num_leds*24; i++) {
        if (colors[i]) {
            sig_gen[idx++]=SEND_HIGH;      // ASSERT HIGH
        } else sig_gen[idx++]=SEND_LOW;    // ASSERT LOW
    }

    // deeeeeeeeeep dive

    int length = sizeof(sig_gen)/sizeof(uint8_t);length+=0;

    // __enable_irq();

    HAL_TIM_PWM_Start_DMA(&htim4, TIM_CHANNEL_1, (uint32_t*) sig_gen, (uint16_t) (num_leds*24) );
    send_semphr=0; // take
    while (!send_semphr) {  } 

    return STATUS_SUCCESS;
}

status_t WS2812B_Reset(GPIO_TypeDef* port, uint16_t pin) {
    uint8_t low_reset[RST_NUM_PERIODS]={0};
    HAL_TIM_PWM_Start_DMA(&htim4, TIM_CHANNEL_1, (uint32_t*) low_reset, RST_NUM_PERIODS);
    send_semphr=0; // take
    while (!send_semphr) { }

    return STATUS_SUCCESS;
}


status_t WS2812B_Init(GPIO_TypeDef* port, uint16_t pin) {
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_DMA_Init();
    MX_TIM4_Init();

    return STATUS_SUCCESS;
}

void HAL_TIM_PWM_PulseFinishedHalfCpltCallback(TIM_HandleTypeDef* htim) { send_semphr=1; }

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef* htim)
{
	HAL_TIM_PWM_Stop_DMA(htim, TIM_CHANNEL_1);
  send_semphr=1; // release
}

